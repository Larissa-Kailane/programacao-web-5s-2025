//variáveis
let num_A = 6;
let num_B = 3;

//soma
resultado = 6 + 3;
minhaString = `Resultado da soma entre ${num_A} e ${num_B} é ${resultado}`;
console.log(minhaString);

//multiplicação
resultado = 6 * 3;
minhaString = `Resultado da multiplicação entre ${num_A} e ${num_B} é ${resultado}`;
console.log(minhaString);

//divisão
resultado = 6 / 3;
minhaString = `Resultado da divisão entre ${num_A} e ${num_B} é ${resultado}`;
console.log(minhaString);

//subtração
resultado = 6 - 3;
minhaString = `Resultado da subtração entre ${num_A} e ${num_B} é ${resultado}`;
console.log(minhaString);

 